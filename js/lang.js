let currentLang = localStorage.getItem('umade_lang') || 'en';

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('umade_lang', lang);
    updatePageLanguage();
    updateLanguageToggle();
    updateHTMLLang();
}

function getText(key) {
    const keys = key.split('.');
    let value = translations[currentLang];
    for (const k of keys) {
        if (value && value[k]) {
            value = value[k];
        } else {
            value = translations['en'];
            for (const k2 of keys) {
                if (value && value[k2]) {
                    value = value[k2];
                } else {
                    return key;
                }
            }
            return value;
        }
    }
    return value;
}

function updatePageLanguage() {
    document.querySelectorAll('[data-lang]').forEach(el => {
        const key = el.dataset.lang;
        const text = getText(key);
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
            el.placeholder = text;
        } else {
            el.textContent = text;
        }
    });
}

function updateLanguageToggle() {
    const toggle = document.getElementById('langToggle');
    if (toggle) {
        const langLabels = { 'en': 'EN', 'ta': 'தமிழ்', 'hi': 'हिंदी' };
        toggle.querySelector('.lang-current').textContent = langLabels[currentLang] || 'EN';
    }
}

function updateHTMLLang() {
    document.documentElement.lang = currentLang;
    if (currentLang === 'ta' || currentLang === 'hi') {
        document.body.classList.add('regional-lang');
    } else {
        document.body.classList.remove('regional-lang');
    }
}

function toggleLanguage() {
    const langs = ['en', 'ta', 'hi'];
    const currentIndex = langs.indexOf(currentLang);
    const nextIndex = (currentIndex + 1) % langs.length;
    setLanguage(langs[nextIndex]);
    showToast('Language changed to ' + (currentLang === 'ta' ? 'Tamil' : currentLang === 'hi' ? 'Hindi' : 'English'));
}

function showToast(message) {
    const toast = document.getElementById('toast');
    const toastMessage = document.getElementById('toastMessage');
    if (toast && toastMessage) {
        toastMessage.textContent = message;
        toast.classList.add('show');
        setTimeout(() => {
            toast.classList.remove('show');
        }, 3000);
    }
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', () => {
    updatePageLanguage();
    updateLanguageToggle();
    updateHTMLLang();
});
